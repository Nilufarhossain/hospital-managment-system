    private void tabletableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabletableMouseClicked

        // TODO add your handling code here:
        int i = tabletable.getSelectedRow();
        TableModel model =tabletable.getModel();
        PatientName.setText(model.getValueAt(i, 1).toString());
        patientEmail.setText(model.getValueAt(i, 2).toString());
        PatientContact.setText(model.getValueAt(i, 3).toString());
        PatientAge.setText(model.getValueAt(i, 4).toString());
        Symptoms.setText(model.getValueAt(i, 5).toString());

        String gender =model.getValueAt(i, 6).toString();
        if(gender.equals("Male"))
        {
            Male.setSelected(true);

        }
        else {
            Female.setSelected(true);

        }
        PatientAddress.setText(model.getValueAt(i, 7).toString());
    }//GEN-LAST:event_tabletableMouseClicked

