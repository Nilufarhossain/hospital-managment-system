    private void UpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_UpdateActionPerformed
        try {

            Class.forName("com.microsoft.sqlserver.jdbc.SQLServerDriver");
            Connection conn = DriverManager.getConnection(
                "jdbc:sqlserver://localhost:1433;databaseName=HospitalManagement;selectMethod=cursor", "sa", "123456");

            //System.out.println("DATABASE NAME IS:" + conn.getMetaData().getDatabaseProductName());
            int row= tabletable.getSelectedRow();
            String value=(tabletable.getModel().getValueAt(row, 0).toString());
            String query = "UPDATE Patient SET PatientName=?,PatientEmail=?, PatientContact=?, PatientAge=?,  Symptoms=?,PatientGender=?,PatientAddress=? where PatientID="+value;

            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, PatientName.getText());
            pst.setString(2, patientEmail.getText());
            pst.setString(3, PatientContact.getText());
            pst.setString(4, PatientAge.getText());
            pst.setString(5, Symptoms.getText());
            
            if(Male.isSelected())
            {
                PatientGender="Male";
            }
            else if(Female.isSelected())
            {
                PatientGender="Female";
            }
            pst.setString(6, PatientGender);
            pst.setString(7, PatientAddress.getText());
            pst.executeUpdate();

            DefaultTableModel  model = (DefaultTableModel)tabletable.getModel();
            model.setRowCount(0);
            show_user();

        }
        catch (Exception e) {

            JOptionPane.showMessageDialog(null, e);
        }// TODO add your handl
    }//GEN-LAST:event_UpdateActionPerformed

